// Function to dynamically import the required swiftness package
export async function loadSwiftnessModule(
    layout: Layout,
    commitment: Commitment,
    stone: Stone,
) {
    const key = `${layout}_${commitment}_${stone}` as keyof VerifierMap;
    if (verifier_map[key]) {
        const [init, parse, verify] = verifier_map[key]!;
        await init();
        return [parse, verify];
    } else {
        throw new Error("Invalid layout | commitment | stone combination");
    }
}
